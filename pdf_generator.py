# ── Markdown → Flowables ──────────────────────────────────────────────────────

def _md_to_flowables(md_text: str, styles: dict) -> list:
    """Convert simple markdown to ReportLab flowables."""
    flowables = []
    lines = md_text.split("\n")
    buffer = []

    def flush_buffer():
        if buffer:
            text = " ".join(buffer).strip()
            if text:
                # Inline bold: **text** → <b>text</b>
                text = re.sub(r"\*\*(.+?)\*\*", r"<b>\1</b>", text)
                flowables.append(Paragraph(text, styles["body"]))
            buffer.clear()

    for line in lines:
        stripped = line.strip()

        if stripped.startswith("## "):
            flush_buffer()
            flowables.append(Paragraph(stripped[3:], styles["h2"]))
            flowables.append(HRFlowable(width="100%", thickness=0.5,
                                         color=INQ_BORDER, spaceAfter=2 * mm))
        elif stripped.startswith("### "):
            flush_buffer()
            flowables.append(Paragraph(stripped[4:], styles["h3"]))
        elif stripped.startswith(("- ", "* ", "• ")):
            flush_buffer()
            bullet_text = stripped[2:].strip()
            bullet_text = re.sub(r"\*\*(.+?)\*\*", r"<b>\1</b>", bullet_text)
            flowables.append(Paragraph(f"• {bullet_text}", styles["bullet"]))
        elif re.match(r"^\d+\.\s", stripped):
            flush_buffer()
            text = re.sub(r"^\d+\.\s", "", stripped)
            text = re.sub(r"\*\*(.+?)\*\*", r"<b>\1</b>", text)
            flowables.append(Paragraph(f"• {text}", styles["bullet"]))
        elif stripped == "" or stripped == "---":
            flush_buffer()
            if stripped == "---":
                flowables.append(HRFlowable(width="100%", thickness=0.5,
                                             color=INQ_BORDER, spaceAfter=2 * mm))
        else:
            buffer.append(stripped)

    flush_buffer()
    return flowables


# ── Public API ────────────────────────────────────────────────────────────────

def generate_pdf(topic: str, report: str, sources: list[dict]) -> bytes:
    """
    Generate a whitepaper-style PDF and return raw bytes.

    Parameters
    ----------
    topic   : research topic string
    report  : markdown report text
    sources : list of {"title": ..., "url": ...}
    """
    buf = io.BytesIO()
    styles = _build_styles()

    # Document with header/footer page template
    doc = BaseDocTemplate(
        buf,
        pagesize=A4,
        leftMargin=20 * mm,
        rightMargin=20 * mm,
        topMargin=25 * mm,
        bottomMargin=20 * mm,
    )
    frame = Frame(
        doc.leftMargin,
        doc.bottomMargin,
        doc.width,
        doc.height,
        id="main",
    )
    doc.addPageTemplates([PageTemplate(id="main", frames=[frame],
                                       onPage=_header_footer)])

    story = []

    # ── Cover / Title block ───────────────────────────────────────────────────
    story.append(Spacer(1, 12 * mm))
    story.append(Paragraph("INQUIRA AI", ParagraphStyle(
        "brand", fontName="Helvetica-Bold", fontSize=10,
        textColor=INQ_ACCENT, spaceAfter=4 * mm,
    )))
    story.append(Paragraph(topic, styles["title"]))
    story.append(Paragraph("Automated Multi-Agent Research Report", styles["subtitle"]))
    story.append(Paragraph(
        f"Generated: {datetime.now().strftime('%B %d, %Y at %H:%M UTC')}",
        styles["meta"],
    ))
    story.append(HRFlowable(width="100%", thickness=1.5, color=INQ_BLUE,
                             spaceAfter=8 * mm))

    # ── Report body ───────────────────────────────────────────────────────────
    story.extend(_md_to_flowables(report, styles))

    # ── Sources / References ──────────────────────────────────────────────────
    if sources:
        story.append(Spacer(1, 8 * mm))
        story.append(Paragraph("Sources & References", styles["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=INQ_BORDER,
                                 spaceAfter=3 * mm))

        for i, src in enumerate(sources, 1):
            title = src.get("title", "Untitled") or "Untitled"
            url = src.get("url", "") or ""
            story.append(KeepTogether([
                Paragraph(f"[{i}] {title}", styles["source_title"]),
                Paragraph(
                    f'<a href="{url}" color="#2563EB">{url}</a>',
                    styles["source_link"],
                ),
                Spacer(1, 1 * mm),
            ]))

    doc.build(story)
    return buf.getvalue()