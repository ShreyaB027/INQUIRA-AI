"""
pdf_generator.py – Professional whitepaper-style PDF using ReportLab.
"""

import io
import re
from datetime import datetime

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm
from reportlab.platypus import (
    BaseDocTemplate,
    Frame,
    PageTemplate,
    Paragraph,
    Spacer,
    HRFlowable,
    KeepTogether,
)
from reportlab.platypus.flowables import HRFlowable
from reportlab.lib.enums import TA_LEFT, TA_CENTER, TA_JUSTIFY
from reportlab.pdfbase import pdfmetrics
from reportlab.pdfbase.ttfonts import TTFont


# ── Palette ───────────────────────────────────────────────────────────────────

INQ_DARK   = colors.HexColor("#0D1117")
INQ_BLUE   = colors.HexColor("#2563EB")
INQ_LIGHT  = colors.HexColor("#F8FAFC")
INQ_GRAY   = colors.HexColor("#64748B")
INQ_BORDER = colors.HexColor("#E2E8F0")
INQ_ACCENT = colors.HexColor("#7C3AED")


# ── Page callbacks ────────────────────────────────────────────────────────────

def _header_footer(canvas, doc):
    canvas.saveState()
    w, h = A4

    # Header bar
    canvas.setFillColor(INQ_DARK)
    canvas.rect(0, h - 18 * mm, w, 18 * mm, fill=1, stroke=0)

    canvas.setFillColor(INQ_LIGHT)
    canvas.setFont("Helvetica-Bold", 11)
    canvas.drawString(20 * mm, h - 11 * mm, "INQUIRA AI")
    canvas.setFont("Helvetica", 9)
    canvas.setFillColor(INQ_GRAY)
    canvas.drawRightString(w - 20 * mm, h - 11 * mm, "Research Report")

    # Footer
    canvas.setFillColor(INQ_BORDER)
    canvas.rect(0, 0, w, 12 * mm, fill=1, stroke=0)
    canvas.setFillColor(INQ_GRAY)
    canvas.setFont("Helvetica", 8)
    canvas.drawString(20 * mm, 4.5 * mm,
                      f"Generated by Inquira AI  •  {datetime.now().strftime('%B %d, %Y')}")
    canvas.drawRightString(w - 20 * mm, 4.5 * mm, f"Page {doc.page}")

    canvas.restoreState()


